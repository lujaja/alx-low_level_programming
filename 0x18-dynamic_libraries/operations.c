#include "main.h"
/**
 * add - function
 * @a: integer
 * @b: integer
 *
 * Return: Sum
 * Description: sum function
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - function
 * @a: integer
 * @b: integer
 *
 * Return: Subtraction
 * Description: sum function
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - function
 * @a: integer
 * @b: integer
 *
 * Return: product
 * Description: multiplication function
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - function
 * @a: integer
 * @b: integer
 *
 * Return: quontient
 * Description: division function
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - function
 * @a: integer
 * @b: integer
 *
 * Return: remainder
 * Description: remainder function
 */
int mod(int a, int b)
{
	return (a % b);
}
