#include "3-calc.h"

/**
 * op_add - Function.
 *
 * @a: Argument.
 * @b: Argument.
 * Return: results.
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Function.
 *
 * @a: Argument
 * @b; Argument
 * Return: Results
 */
int op_sub(int a, int b)
{
	return (a -b);
}
/**
 * op_mul - Function.
 *
 * @a: Argument.
 * @b: Argument.
 * Return: results.
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Function.
 *
 * @a: Argument
 * @b: Argument
 * Return: results
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Function.
 *
 * @a: Argument.
 * @b; Argument.
 * Return: Results.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
